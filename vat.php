<?php

// Check if the form has been submitted
if (isset($_POST['submit'])) {

  // Get the amount from the form input
  $amount = $_POST['amount'];

  // Calculate the VAT (15% of the amount)
  $vat = $amount * 0.15;

  // Calculate the total amount including VAT
  $total = $amount + $vat;

  // Output the results
  echo "Amount: $" . $amount . "\n";
  echo "VAT (15%): $" . $vat . "\n";
  echo "Total: $" . $total . "\n";
}

?>

<!-- HTML form for inputting the amount -->
<form method="post">
  <label for="amount">Amount:</label>
  <input type="number" name="amount" id="amount">
  <button type="submit" name="submit">Calculate VAT</button>
</form>
